package org.example;

public class Length {
    public static void main(String[] args) {
        String myName = "ჩემი სახელია ნათია";
        if (myName.length() % 2 == 0) {
            System.out.println("სტრიქონის სიგრძე ლუწია");
        } else {
            System.out.println("სტრიქონის სიგრძე კენტია");
        }
    }
}